<script>
import { defineComponent } from 'vue'
import ActionSection from '@/Components/ActionSection.vue'
import ConfirmPassword from '@/Components/ConfirmPassword.vue'

export default defineComponent({
    components: {
        ActionSection,
        ConfirmPassword,
    },

    data() {
        return {
            enabling: false,
            disabling: false,

            qrCode: null,
            recoveryCodes: [],
        }
    },

    methods: {
        enableTwoFactorAuthentication() {
            this.enabling = true

            this.$inertia.post('/user/two-factor-authentication', {}, {
                preserveScroll: true,
                onSuccess: () => Promise.all([
                    this.showQrCode(),
                    this.showRecoveryCodes(),
                ]),
                onFinish: () => (this.enabling = false),
            })
        },

        showQrCode() {
            return axios.get('/user/two-factor-qr-code')
                    .then(response => {
                        this.qrCode = response.data.svg
                    })
        },

        showRecoveryCodes() {
            return axios.get('/user/two-factor-recovery-codes')
                    .then(response => {
                        this.recoveryCodes = response.data
                    })
        },

        regenerateRecoveryCodes() {
            axios.post('/user/two-factor-recovery-codes')
                    .then(response => {
                        this.showRecoveryCodes()
                    })
        },

        disableTwoFactorAuthentication() {
            this.disabling = true

            this.$inertia.delete('/user/two-factor-authentication', {
                preserveScroll: true,
                onSuccess: () => (this.disabling = false),
            })
        },
    },

    computed: {
        twoFactorEnabled() {
            return ! this.enabling && this.$page.props.user.two_factor_enabled
        }
    }
})
</script>

<template>
    <ActionSection>
        <template #title>
            Two Factor Authentication
        </template>

        <template #description>
            Add additional security to your account using two factor authentication.
        </template>

        <template #content>
            <h3 class="text-lg font-medium" v-if="twoFactorEnabled">
                You have enabled two factor authentication.
            </h3>

            <h3 class="text-lg font-medium" v-else>
                You have not enabled two factor authentication.
            </h3>

            <div class="max-w-xl mt-3 text-sm opacity-70">
                <p>
                    When two factor authentication is enabled, you will be prompted for a secure, random token during authentication. You may retrieve this token from your phone's Google Authenticator application.
                </p>
            </div>

            <div v-if="twoFactorEnabled">
                <div v-if="qrCode">
                    <div class="max-w-xl mt-4 text-sm opacity-50 text-base-content">
                        <p class="font-semibold">
                            Two factor authentication is now enabled. Scan the following QR code using your phone's authenticator application.
                        </p>
                    </div>

                    <div class="mt-4" v-html="qrCode"> </div>
                </div>

                <div v-if="recoveryCodes.length > 0">
                    <div class="max-w-xl mt-4 text-sm opacity-50 text-base-content">
                        <p class="font-semibold">
                            Store these recovery codes in a secure password manager. They can be used to recover access to your account if your two factor authentication device is lost.
                        </p>
                    </div>

                    <div class="grid max-w-xl gap-1 px-4 py-4 mt-4 font-mono text-sm rounded-lg bg-base-100">
                        <div v-for="code in recoveryCodes" :key="code">
                            {{ code }}
                        </div>
                    </div>
                </div>
            </div>

            <div class="mt-5">
                <div v-if="! twoFactorEnabled">
                    <ConfirmPassword @confirmed="enableTwoFactorAuthentication">
                        <button class="btn btn-secondary btn-sm" type="button" :disabled="enabling">
                            Enable
                        </button>
                    </ConfirmPassword>
                </div>

                <div v-else>
                    <ConfirmPassword @confirmed="regenerateRecoveryCodes">
                        <button class="mr-3 btn btn-secondary btn-sm"
                                        v-if="recoveryCodes.length > 0">
                            Regenerate Recovery Codes
                        </button>
                    </ConfirmPassword>

                    <ConfirmPassword @confirmed="showRecoveryCodes">
                        <button class="mr-3 btn btn-secondary btn-sm" v-if="recoveryCodes.length === 0">
                            Show Recovery Codes
                        </button>
                    </ConfirmPassword>

                    <ConfirmPassword @confirmed="disableTwoFactorAuthentication">
                        <button
                            class="btn btn-error btn-sm"
                            :disabled="disabling">
                            Disable
                        </button>
                    </ConfirmPassword>
                </div>
            </div>
        </template>
    </ActionSection>
</template>
